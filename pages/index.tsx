import {
  Heading,
  Input,
  InputGroup,
  InputRightElement,
  IconButton,
} from "@chakra-ui/react";
import { Search2Icon } from "@chakra-ui/icons";

import React, { useState } from "react";
import { useRouter } from "next/router";
import Head from "next/head";
import type { NextPage } from "next";
import Image from "next/image";

const Home: NextPage = () => {
  const router = useRouter();
  const [query, setQuery] = useState("");

  const handleChange = (event: React.FormEvent<HTMLInputElement>) => {
    setQuery(event.currentTarget.value);
  };

  const handleSubmit = (event: React.FormEvent) => {
    event.preventDefault();

    router.push(`/eat/${query}`);

    setQuery("");
  };

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex flex-col lg:flex-row lg:gap-36 flex-auto justify-center items-center px-8">
        <section className="flex-col">
          <Heading>
            Find <span className="text-red-500">tasty</span> food you love
          </Heading>
          <form className="py-3" onSubmit={handleSubmit}>
            <InputGroup>
              <Input
                value={query}
                onChange={handleChange}
                placeholder="pancakes, sushi, etc."
              />
              <InputRightElement
                children={
                  <IconButton
                    colorScheme="gray"
                    aria-label="Search database"
                    icon={<Search2Icon />}
                  />
                }
                color="gray.300"
                fontSize="1.2em"
              />
            </InputGroup>
          </form>
        </section>
        <section className="w-full lg:w-auto flex justify-center items-center">
          <Image
            src="/pancake.png"
            width="500"
            height="400"
            alt="Pancakes. Yum!"
          />
        </section>
      </main>
    </>
  );
};

export default Home;
